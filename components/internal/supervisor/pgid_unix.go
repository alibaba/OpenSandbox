//go:build unix

package supervisor

import (
	"os/exec"
	"syscall"
)

// applyChildPgid puts the worker in its own process group so that signals
// sent to the supervisor's pgid (e.g. by a parent process tree) do not
// accidentally reach the worker. The supervisor sends explicit signals to
// the worker pid when it intends to.
func applyChildPgid(cmd *exec.Cmd) {
	if cmd.SysProcAttr == nil {
		cmd.SysProcAttr = &syscall.SysProcAttr{}
	}
	cmd.SysProcAttr.Setpgid = true
}
